  const SongEnum({
    required this.name,
    required this.path,
    required this.album,
    required this.performers,
  });

  final String name;
  final String path;
  final AlbumEnum album;
  final List<PerformerEnum> performers;

  String get getPerformersString {
    if (performers.isEmpty) return '';
    if (performers.length == 1) return performers[0].name;

    String str = performers[0].name;
    for (var i = 1; i < performers.length; i++) {
      str += " & ${performers[i].name}";
    }
    return str;
  }
